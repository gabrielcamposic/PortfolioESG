def publish_derived_snapshots() -> None:
    """Generate snapshot JSONs from the latest row of their history CSVs.

    These files used to be written directly by A4_Analysis.py.  Now A4 only
    appends to the history CSVs and D_Publish derives the snapshot JSONs,
    eliminating the duplication.

    Generated files (in data/results/):
      - portfolio_diagnostics.json   ← from portfolio_diagnostics_history.csv
      - performance_attribution.json ← from performance_attribution_history.csv
    """
    logger.info("Step 0: Generating derived snapshot JSONs from history CSVs")

    # --- portfolio_diagnostics.json ---
    diag = latest_csv_rows(DIAGNOSTICS_HISTORY_CSV)
    if not diag.empty:
        row = diag.iloc[0]
        payload = {k: (None if pd.isna(v) else v) for k, v in row.to_dict().items()}
        write_json(DIAGNOSTICS_JSON, payload)
        ensure_symlink(DIAGNOSTICS_JSON, DST)
    else:
        logger.warning("  No diagnostics history data — skipping portfolio_diagnostics.json")

    # --- performance_attribution.json ---
    attr = latest_csv_rows(ATTRIBUTION_HISTORY_CSV)
    if not attr.empty:
        row = attr.iloc[0]
        payload = {k: (None if pd.isna(v) else v) for k, v in row.to_dict().items()}
        write_json(ATTRIBUTION_JSON, payload)
        ensure_symlink(ATTRIBUTION_JSON, DST)
    else:
        logger.warning("  No attribution history data — skipping performance_attribution.json")


