# STEP 4b3: COMPUTE BROKER RETURN (Modified Dietz Monthly)
# ═══════════════════════════════════════════════════════════════════════════════


def _compute_broker_return(daily_df: pd.DataFrame) -> dict:
    """Compute Modified Dietz monthly return approximating the broker's MWR view.

    Tracks the TOTAL brokerage account (stocks + idle cash) and weights
    external flows (deposits, withdrawals, fund transfers) by their time
    in the month.

    Data sources:
      - portfolio_real_daily.csv  → daily stock portfolio values
      - ledger.csv                → buy/sell trade flows (internal cash ↔ stocks)
      - cash_movements.csv        → external deposits, withdrawals, dividends, fund transfers

    Returns a dict with method, monthly breakdown, and compounded total.
    """
    if daily_df.empty:
        logger.warning("  Broker return: no daily data — skipping")
        return {}

    # ── Load cash movements ──
    cash_df = pd.DataFrame()
    if CASH_MOVEMENTS_CSV.exists():
        try:
            cash_df = pd.read_csv(CASH_MOVEMENTS_CSV)
            logger.info(f"  Loaded {len(cash_df)} cash movement(s) from {CASH_MOVEMENTS_CSV.name}")
        except Exception as e:
            logger.warning(f"  Could not read {CASH_MOVEMENTS_CSV.name}: {e}")
    else:
        logger.warning("  cash_movements.csv not found — broker return will use heuristic")

    # ── Load ledger ──
    ledger_df = pd.DataFrame()
    if LEDGER_CSV.exists():
        try:
            ledger_df = pd.read_csv(LEDGER_CSV)
        except Exception:
            pass

    # ── Build month-level stock values from daily series ──
    daily = daily_df.copy()
    daily["date_dt"] = pd.to_datetime(daily["date"])
    daily["month"] = daily["date_dt"].dt.to_period("M")
    daily["pv"] = pd.to_numeric(daily["portfolio_value"], errors="coerce")

    month_stock: Dict[str, dict] = {}
    for period, grp in daily.groupby("month"):
        month_stock[str(period)] = {
            "last_value": float(grp["pv"].iloc[-1]),
            "trading_days": len(grp),
        }

    # ── Build ledger net cash flows by date ──
    # net_cash_flow: negative = cash out (buy), positive = cash in (sell)
    ledger_flows: Dict[str, float] = {}
    if not ledger_df.empty:
        for _, row in ledger_df.iterrows():
            dt = str(row.get("trade_date", "")).strip()[:10]
            ncf = safe_float(row.get("net_cash_flow"), 0.0)
            ledger_flows[dt] = ledger_flows.get(dt, 0.0) + ncf

    # ── Pre-compute cumulative net shares from ledger ──
    # Used to detect full liquidation (e.g., Nov 11 sell-all).
    # We use sum(all values) not sum(max(0,v)) because corporate events
    # may cause ticker name changes (e.g., "VULCABRAS ON NM" → "VULCABRAS ON EDS NM"),
    # creating offset entries that should cancel.
    cumulative_shares: Dict[str, float] = {}
    if not ledger_df.empty:
        ldf = ledger_df.copy()
        ldf["_dt"] = pd.to_datetime(ldf["trade_date"])
        ldf = ldf.sort_values("_dt")
        running_shares: Dict[str, float] = {}
        for _, row in ldf.iterrows():
            ticker = str(row.get("ticker", ""))
            side = str(row.get("side", "")).upper()
            qty = safe_float(row.get("quantity"), 0.0)
            if side == "BUY":
                running_shares[ticker] = running_shares.get(ticker, 0.0) + qty
            elif side == "SELL":
                running_shares[ticker] = running_shares.get(ticker, 0.0) - qty
            dt = str(row.get("trade_date", "")).strip()[:10]
            # Total net shares across ALL tickers (allows negatives to cancel)
            total = sum(running_shares.values())
            cumulative_shares[dt] = total

    # ── Build external cash movements by date ──
    external_by_date: Dict[str, List] = {}
    if not cash_df.empty:
        for _, row in cash_df.iterrows():
            dt = str(row.get("date", "")).strip()[:10]
            typ = str(row.get("type", ""))
            amt = safe_float(row.get("amount"), 0.0)
            external_by_date.setdefault(dt, []).append((typ, amt))

    # ── Determine month range (first trade → last data) ──
    first_date = daily["date_dt"].min()
    last_date = daily["date_dt"].max()
    all_months = pd.period_range(
        start=first_date.to_period("M"),
        end=last_date.to_period("M"),
        freq="M",
    )

    # ── Compute Modified Dietz per month ──
    cash_balance = 0.0
    prev_stock_value = 0.0
    monthly_results: Dict[str, dict] = {}

    for month in all_months:
        month_str = str(month)  # "YYYY-MM"
        year, mo = month.year, month.month
        cd = calendar.monthrange(year, mo)[1]  # calendar days in month

        # Starting values for this month
        v_start = prev_stock_value + cash_balance

        # Process all dates in this month
        month_external_flows: List[tuple] = []  # (day_of_month, amount)

        for day in range(1, cd + 1):
            dt = f"{year:04d}-{mo:02d}-{day:02d}"

            # Ledger trades (internal: cash ↔ stocks, not external flows)
            if dt in ledger_flows:
                cash_balance += ledger_flows[dt]

            # External cash movements
            if dt in external_by_date:
                for typ, amt in external_by_date[dt]:
                    if typ == "DEPOSIT":
                        cash_balance += amt
                        month_external_flows.append((day, amt))
                    elif typ in ("WITHDRAWAL", "FUND_TRANSFER"):
                        cash_balance += amt  # amt is already negative
                        month_external_flows.append((day, amt))
                    elif typ == "DIVIDEND":
                        cash_balance += amt  # income, NOT an external flow

        # End-of-month stock value
        stock_end = month_stock.get(month_str, {}).get("last_value", 0.0)
        trading_days = month_stock.get(month_str, {}).get("trading_days", 0)

        # Check if portfolio was fully liquidated during this month
        # by looking at cumulative net shares from the ledger.
        # Find the latest ledger date within this month.
        month_prefix = month_str  # "YYYY-MM"
        month_ledger_dates = sorted(
            d for d in cumulative_shares if d.startswith(month_prefix)
        )
        if month_ledger_dates:
            last_ledger_date = month_ledger_dates[-1]
            if cumulative_shares[last_ledger_date] < 1:
                # Portfolio was fully liquidated during this month
                stock_end = 0.0

        v_end = stock_end + cash_balance

        # Modified Dietz formula
        sum_cf = sum(amt for _, amt in month_external_flows)
        sum_wcf = sum(((cd - d) / cd) * amt for d, amt in month_external_flows)

        gain = v_end - v_start - sum_cf
        denom = v_start + sum_wcf

        if abs(denom) > 0.01:
            r_month = gain / denom
        else:
            r_month = 0.0

        monthly_results[month_str] = {
            "return": round(r_month, 6),
            "trading_days": trading_days,
            "v_start": round(v_start, 2),
            "v_end": round(v_end, 2),
            "cash_balance": round(cash_balance, 2),
            "external_flows": round(sum_cf, 2),
        }

        logger.info(
            f"  Dietz {month_str}: V_start={v_start:,.2f} V_end={v_end:,.2f} "
            f"CF={sum_cf:,.2f} r={r_month:+.4f} cash={cash_balance:,.2f}"
        )

        prev_stock_value = stock_end

    # ── Compound monthly returns ──
    total = 1.0
    for m_data in monthly_results.values():
        total *= (1 + m_data["return"])
    total -= 1

    logger.info(f"  Broker return (Modified Dietz): {total:+.4f} ({total*100:+.2f}%)")

    return {
        "method": "modified_dietz_monthly",
        "monthly": monthly_results,
        "total": round(total, 6),
        "note": "Monthly Modified Dietz using real deposit/withdrawal data from Ágora statements.",
    }


def _compute_risk_windows(daily_df: pd.DataFrame, hist_df: pd.DataFrame) -> dict:
    """Compute Tracking Error, Information Ratio, and HHI per time window.

    Windows: All, YTD, 3M, 6M, 12M, 24M.
    TE and IR are derived from daily returns in daily_df.
    HHI is a point-in-time snapshot from the last date in hist_df within each window.
    """
    if daily_df.empty:
        return {}

    df = daily_df.copy()
    df["date_dt"] = pd.to_datetime(df["date"])
    df["port_r"] = pd.to_numeric(df["portfolio_return"], errors="coerce")
    df["bench_r"] = pd.to_numeric(df["benchmark_return"], errors="coerce")

    last_date = df["date_dt"].max()

    # Prepare hist_df for HHI lookups
    hist_has_data = hist_df is not None and not hist_df.empty
    if hist_has_data:
        hist_df = hist_df.copy()
        hist_df["date_dt"] = pd.to_datetime(hist_df["date"])
        hist_df["value_f"] = pd.to_numeric(hist_df["value"], errors="coerce")

    windows = {
        "All": df["date_dt"].min(),
        "YTD": pd.Timestamp(last_date.year, 1, 1),
        "3M": last_date - pd.DateOffset(months=3),
        "6M": last_date - pd.DateOffset(months=6),
        "12M": last_date - pd.DateOffset(months=12),
        "24M": last_date - pd.DateOffset(months=24),
    }

    result = {}

    for label, start in windows.items():
        subset = df[df["date_dt"] >= start]
        port_r = subset["port_r"].dropna()
        bench_r = subset["bench_r"].dropna()

        if len(port_r) < 2 or len(bench_r) < 2:
            continue

        # Align to common dates
        common_idx = port_r.index.intersection(bench_r.index)
        port_common = port_r.loc[common_idx]
        bench_common = bench_r.loc[common_idx]
        n = len(port_common)

        if n < 2:
            continue

        # Tracking Error
        diff = port_common.values - bench_common.values
        te = float(np.std(diff, ddof=1) * np.sqrt(252))

        # Alpha annual
        port_total = float(np.prod(1 + port_common.values) - 1)
        bench_total = float(np.prod(1 + bench_common.values) - 1)
        port_annual = float((1 + port_total) ** (252 / n) - 1) if n > 0 else 0.0
        bench_annual = float((1 + bench_total) ** (252 / n) - 1) if n > 0 else 0.0
        alpha_annual = port_annual - bench_annual

        # Information Ratio
        ir = float(alpha_annual / te) if te > 0 else None

        # HHI: point-in-time from last date in hist_df within this window
        hhi = None
        n_assets = None
        if hist_has_data:
            hist_window = hist_df[hist_df["date_dt"] >= start]
            if not hist_window.empty:
                last_hist_date = hist_window["date_dt"].max()
                last_day = hist_window[hist_window["date_dt"] == last_hist_date]
                values = last_day["value_f"].dropna()
                total_val = values.sum()
                if total_val > 0:
                    w = values / total_val
                    hhi = round(float((w ** 2).sum()), 4)
                    n_assets = int((values > 0).sum())

        entry: Dict[str, Any] = {
            "tracking_error": round(te, 4),
            "information_ratio": round(ir, 2) if ir is not None else None,
            "hhi": hhi,
            "n_assets": n_assets,
            "trading_days": n,
        }
        result[label] = entry

    return result


