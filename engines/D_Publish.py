def publish_scored_targets() -> Dict[str, float]:
    """Generate scored_targets.json from latest scored_stocks.csv run."""
    logger.info("Step 2: Generating scored_targets.json")

    targets: Dict[str, float] = {}
    symbols: Dict[str, str] = {}

    # Latest close prices used to sanity-check target prices (cap upside to 1000% → 11x)
    price_map = _load_latest_prices()

    latest = latest_csv_rows(SCORED_STOCKS_CSV)
    if latest.empty:
        logger.warning("  No scored stocks data available")
        write_json(SCORED_TARGETS_JSON, {
            "targets": {},
            "symbols": {},
            "generated_at": datetime.now(timezone.utc).isoformat(),
        })
        ensure_symlink(SCORED_TARGETS_JSON, DST)
        return targets

    for _, row in latest.iterrows():
        stock = str(row.get("Stock", "")).strip()
        if not stock:
            continue
        tp = safe_float(row.get("TargetPrice"), 0.0)
        if tp <= 0:
            continue

        # Cap unrealistic targets if they imply >1000% upside relative to latest close
        price = price_map.get(stock)
        if price is None:
            # try normalized lookup
            norm = normalize_ticker(stock)
            for k, v in price_map.items():
                if normalize_ticker(k) == norm:
                    price = v
                    break
        if price and price > 0:
            max_target = price * 11  # 1000% upside cap (UpsidePotential <= 10)
            if tp > max_target:
                logger.warning(
                    f"  Skipping target for {stock}: {tp:.2f} > cap {max_target:.2f} (price {price:.2f})"
                )
                continue

        key = normalize_ticker(stock)
        targets[key] = tp
        symbols[key] = stock

    write_json(SCORED_TARGETS_JSON, {
        "targets": targets,
        "symbols": symbols,
        "generated_at": datetime.now(timezone.utc).isoformat(),
    })
    ensure_symlink(SCORED_TARGETS_JSON, DST)
    return targets


