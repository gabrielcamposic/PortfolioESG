def safe_float(v: Any, default: float = 0.0) -> float:
    """Safely convert a value to float."""
    if v is None or v == "":
        return default
    try:
        return float(v)
    except (TypeError, ValueError):
        return default


def sanitize_for_json(value: Any) -> Any:
    """Replace NaN/Inf with None for JSON serialization."""
    if isinstance(value, dict):
        return {k: sanitize_for_json(v) for k, v in value.items()}
    if isinstance(value, list):
        return [sanitize_for_json(v) for v in value]
    if isinstance(value, float):
        if not math.isfinite(value):
            return None
    return value


def write_json(path: Path, payload: dict) -> None:
    """Write JSON with NaN sanitization."""
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w", encoding="utf-8") as f:
        json.dump(sanitize_for_json(payload), f, indent=2, ensure_ascii=False, allow_nan=False)
    logger.info(f"  Wrote {path.name}")


def ensure_symlink(src: Path, dst_dir: Path, dst_name: Optional[str] = None) -> bool:
    """Create a symlink in dst_dir pointing to src.  Returns True on success.

    If the destination already exists:
      - If it's a symlink pointing to src → no-op
      - If it's a symlink pointing elsewhere → replace
      - If it's a regular file → remove and replace with symlink
    Uses *absolute* paths so the link works regardless of cwd.
    """
    if not src.exists():
        logger.warning(f"  Source not found, skipping: {src.name}")
        return False

    src_abs = src.resolve()
    dst = dst_dir / (dst_name or src.name)
    dst.parent.mkdir(parents=True, exist_ok=True)

    # Already a correct symlink?
    if dst.is_symlink():
        try:
            if dst.resolve() == src_abs:
                logger.info(f"  {dst.name} ✓ symlink OK")
                return True
        except OSError:
            pass
        # Wrong target — remove and recreate
        dst.unlink()

    # Regular file occupying the slot — remove it
    if dst.exists():
        dst.unlink()

    os.symlink(src_abs, dst)
    logger.info(f"  {dst.name} → {src_abs}")
    return True


def read_json_safe(path: Path) -> dict:
    """Read a JSON file, returning empty dict if missing or corrupt."""
    if not path.exists():
        return {}
    try:
        with open(path, "r", encoding="utf-8") as f:
            return json.load(f)
    except (json.JSONDecodeError, OSError) as e:
        logger.warning(f"  Could not read {path.name}: {e}")
        return {}


def latest_csv_rows(path: Path, run_id_col: str = "run_id") -> pd.DataFrame:
    """Read a CSV and return only rows from the latest run_id."""
    if not path.exists():
        return pd.DataFrame()
    try:
        df = pd.read_csv(path)
        if run_id_col not in df.columns or df.empty:
            return df
        latest = df[run_id_col].max()
        return df[df[run_id_col] == latest].copy()
    except Exception as e:
        logger.warning(f"  Could not read {path.name}: {e}")
        return pd.DataFrame()


def normalize_ticker(s: Optional[str]) -> str:
    """Normalize string for ticker matching (uppercase, alphanum only)."""
    if not s:
        return ""
    return "".join(c for c in str(s).upper() if c.isalnum())


def resolve_source(canonical: Path, fallback_name: Optional[str] = None) -> Path:
    """Return canonical path if it exists, otherwise fall back to html/data/.

    During the transition period some files may only exist in html/data/ because
    the pipeline hasn't been fully re-run yet.  This helper transparently picks
    whichever copy is available (preferring canonical).
    """
    if canonical.exists():
        return canonical
    fb = DST / (fallback_name or canonical.name)
    if fb.exists():
        logger.debug(f"  Fallback: using {fb} (canonical {canonical} not yet created)")
        return fb
    return canonical  # caller will handle missing file


