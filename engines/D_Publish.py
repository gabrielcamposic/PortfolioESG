def main() -> int:
    start_time = datetime.now()
    logger.info(f"D_Publish.py v{D_PUBLISH_VERSION} — Starting frontend asset publication")
    logger.info(f"Source: {SRC}")
    logger.info(f"Target: {DST}")

    DST.mkdir(parents=True, exist_ok=True)

    try:
        # Step 0: Generate derived snapshot JSONs from history CSVs
        publish_derived_snapshots()

        # Step 1: Symlink canonical files
        publish_symlink_files()

        # Step 2: Generate scored_targets.json
        target_map = publish_scored_targets()

        # Step 3: Generate pipeline_latest.json
        publish_pipeline_latest(target_map)

        # Step 4a: Generate portfolio_real_daily.csv (Mudança 1)
        daily_df = _build_real_daily_series()

        # Step 4b: Compute real TWR metrics (Mudança 2)
        real_metrics = _compute_real_metrics(daily_df)

        # Step 4b2: Compute risk windows (TE, IR, HHI per time window)
        hist_df = pd.read_csv(PORTFOLIO_HISTORY_CSV) if PORTFOLIO_HISTORY_CSV.exists() else pd.DataFrame()
        risk_windows = _compute_risk_windows(daily_df, hist_df)

        # Step 4b3: Compute broker return (Modified Dietz with cash tracking)
        broker_return = _compute_broker_return(daily_df)

        # Step 4c: Generate consolidated dashboard JSON (Mudança 3)
        publish_dashboard_latest(real_metrics, risk_windows, broker_return)

        # Step 5: Processed notes
        publish_processed_notes()

        # Step 6: Progress file symlinks
        publish_progress_symlinks()

        elapsed = (datetime.now() - start_time).total_seconds()
        logger.info(f"D_Publish.py completed in {elapsed:.1f}s")
        return 0

    except Exception as e:
        logger.critical(f"D_Publish.py failed: {e}", exc_info=True)
        return 1


if __name__ == "__main__":
    sys.exit(main())



