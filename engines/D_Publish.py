# STEP 4a: GENERATE PORTFOLIO_REAL_DAILY.CSV (Mudança 1)
# ═══════════════════════════════════════════════════════════════════════════════


def _build_real_daily_series() -> pd.DataFrame:
    """Build the real daily returns CSV from portfolio_history + ledger + timeseries.

    Output columns: date, portfolio_value, cost_basis, cash_flow,
                    portfolio_return, benchmark_return, cdi_return

    Returns an empty DataFrame if source data is insufficient.
    """
    logger.info("Step 4a: Building portfolio_real_daily.csv")

    # --- Load portfolio_history.csv → {date: portfolio_value, cost_basis} ---
    if not PORTFOLIO_HISTORY_CSV.exists():
        logger.warning("  portfolio_history.csv not found — skipping real daily series")
        return pd.DataFrame()

    hist = pd.read_csv(PORTFOLIO_HISTORY_CSV)
    if hist.empty:
        return pd.DataFrame()

    # Group by date → one row per date (market_value & cost_basis are already totals)
    hist_daily = hist.groupby("date").agg(
        portfolio_value=("market_value", "first"),
        cost_basis=("cost_basis", "first"),
    ).reset_index().sort_values("date")

    # --- Load ledger.csv → cash flows by date ---
    cf_by_date: Dict[str, float] = {}
    if LEDGER_CSV.exists():
        ledger = pd.read_csv(LEDGER_CSV)
        for _, row in ledger.iterrows():
            dt = str(row.get("trade_date", "")).strip()[:10]
            side = str(row.get("side", "")).upper()
            total_cost = safe_float(row.get("total_cost"), 0.0)
            # BUY = cash out (positive CF into portfolio), SELL = cash in (negative)
            cf = total_cost if side == "BUY" else -total_cost
            cf_by_date[dt] = cf_by_date.get(dt, 0.0) + cf

    # --- Load portfolio_timeseries.csv → benchmark daily returns ---
    bench_by_date: Dict[str, float] = {}
    ts_path = resolve_source(PORTFOLIO_TIMESERIES_CSV)
    if ts_path.exists():
        ts = pd.read_csv(ts_path)
        for _, row in ts.iterrows():
            dt = str(row.get("date", "")).strip()[:10]
            br = safe_float(row.get("benchmark1_daily_return"), None)
            if br is not None:
                bench_by_date[dt] = br

    # --- Load CDI daily returns (real from LFTS11 or SELIC fallback) ---
    cdi_by_date, cdi_source = _load_cdi_daily_returns()
    cdi_is_synthetic = "fallback" in cdi_source
    selic_daily_fallback = (1 + SELIC_ANNUAL_FALLBACK) ** (1 / 252) - 1

    # --- Build daily series (only dates present in portfolio_history, already business-days) ---
    dates = hist_daily["date"].tolist()
    rows = []

    for i, dt in enumerate(dates):
        pv = hist_daily.iloc[i]["portfolio_value"]
        cb = hist_daily.iloc[i]["cost_basis"]
        cf = cf_by_date.get(dt, 0.0)

        if i == 0:
            port_ret = None
            bench_ret = None
            cdi_ret = None
        else:
            prev_pv = hist_daily.iloc[i - 1]["portfolio_value"]
            prev_cb = hist_daily.iloc[i - 1]["cost_basis"]

            # Determine actual cash flow for TWR calculation
            # If cost_basis changed, there was a real transaction
            cost_delta = cb - prev_cb
            if abs(cost_delta) > 0.01:
                # Use cost_basis delta as cash flow (more reliable than ledger
                # because it's already aligned with the tracked portfolio)
                effective_cf = cost_delta
            else:
                effective_cf = 0.0

            # TWR daily return: r = (V_t - CF_t) / V_{t-1} - 1
            if prev_pv > 0:
                port_ret = (pv - effective_cf) / prev_pv - 1
            else:
                port_ret = None

            bench_ret = bench_by_date.get(dt, None)

            # CDI: real data from LFTS11, fallback to synthetic
            cdi_ret = cdi_by_date.get(dt, selic_daily_fallback if cdi_is_synthetic else None)

        rows.append({
            "date": dt,
            "portfolio_value": round(pv, 2),
            "cost_basis": round(cb, 2),
            "cash_flow": round(cf, 2),
            "portfolio_return": round(port_ret, 8) if port_ret is not None else "",
            "benchmark_return": round(bench_ret, 8) if bench_ret is not None else "",
            "cdi_return": round(cdi_ret, 8) if cdi_ret is not None else "",
        })

    df = pd.DataFrame(rows)
    if not df.empty:
        df.to_csv(PORTFOLIO_REAL_DAILY_CSV, index=False)
        logger.info(f"  Wrote {PORTFOLIO_REAL_DAILY_CSV.name} ({len(df)} rows)")
        ensure_symlink(PORTFOLIO_REAL_DAILY_CSV, DST)

    return df


