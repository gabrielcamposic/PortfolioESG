def publish_correlation_heatmap(window_days: int = 252) -> None:
    """Step 7: Compute correlation matrix for current holdings + model tickers.

    Reads tickers from optimized_recommendation.json (holdings + optimal
    weights), loads the last `window_days` of daily closes from StockDataDB,
    and writes correlation_model_portfolio.json to html/data/.

    Ticker ordering in the output:
        [holdings-only] + [in-both] + [model-only]

    so that a heatmap shows the two portfolios as distinct visual blocks.
    """
    logger.info("Step 7: Generating correlation heatmap (holdings vs model)")

    out_path = DST / "correlation_model_portfolio.json"

    # --- Load tickers from recommendation ---
    rec = read_json_safe(resolve_source(OPTIMIZED_RECOMMENDATION_JSON))
    if not rec:
        logger.warning("  optimized_recommendation.json not found — skipping correlation")
        return

    holdings_tickers = set(rec.get("comparison", {}).get("holdings", {}).get("weights", {}).keys())
    model_tickers    = set(rec.get("comparison", {}).get("optimal",  {}).get("weights", {}).keys())
    all_tickers      = sorted(holdings_tickers | model_tickers)

    if not all_tickers:
        logger.warning("  No tickers found in recommendation — skipping correlation")
        return

    logger.info(f"  Holdings: {sorted(holdings_tickers)}")
    logger.info(f"  Model:    {sorted(model_tickers)}")

    # --- Load prices from StockDataDB ---
    if not STOCK_DATA_DB.exists():
        logger.warning(f"  {STOCK_DATA_DB.name} not found — skipping correlation")
        return

    try:
        db = pd.read_csv(STOCK_DATA_DB, parse_dates=["Date"])
        db = db[db["Stock"].isin(all_tickers)][["Date", "Stock", "Close"]].dropna()
        pivot = (
            db.pivot_table(index="Date", columns="Stock", values="Close")
            .sort_index()
            .tail(window_days)
        )
        missing = [t for t in all_tickers if t not in pivot.columns]
        if missing:
            logger.warning(f"  Tickers missing from StockDataDB: {missing}")
            all_tickers = [t for t in all_tickers if t in pivot.columns]
            holdings_tickers -= set(missing)
            model_tickers    -= set(missing)

        returns = pivot[all_tickers].pct_change().dropna()
        corr    = returns.corr().round(4)
        date    = pivot.index[-1].strftime("%Y-%m-%d")
        actual_window = len(returns)
        logger.info(f"  Window: {pivot.index[0].date()} → {date} ({actual_window} return days)")
    except Exception as e:
        logger.error(f"  Failed to compute correlations: {e}")
        return

    # --- Group labels and display order ---
    groups: Dict[str, str] = {}
    for t in all_tickers:
        in_h = t in holdings_tickers
        in_m = t in model_tickers
        groups[t] = "both" if (in_h and in_m) else ("holdings" if in_h else "model")

    order: List[str] = (
        [t for t in sorted(holdings_tickers) if groups[t] == "holdings"] +
        [t for t in sorted(all_tickers)      if groups[t] == "both"]     +
        [t for t in sorted(model_tickers)    if groups[t] == "model"]
    )

    matrix: List[List[Optional[float]]] = []
    for r in order:
        row: List[Optional[float]] = []
        for c in order:
            try:
                v = float(corr.loc[r, c])
                row.append(None if not math.isfinite(v) else round(v, 4))
            except (KeyError, TypeError):
                row.append(None)
        matrix.append(row)

    payload = {
        "date":        date,
        "window_days": actual_window,
        "tickers":     order,
        "groups":      groups,
        "matrix":      matrix,
    }

    write_json(out_path, payload)
    logger.info(f"  Saved {out_path.name} ({len(order)}×{len(order)} matrix)")


def publish_history_enriched() -> None:
    """Step 8: Generate portfolio_history_enriched.json for the History page.

    Merges data from:
      - portfolio_results_db.csv       (A3 engine: backtest metrics)
      - optimized_portfolio_history.jsonl  (C engine: forward-looking metrics)
      - latest_run_summary.json        (momentum for the latest run)

    Output: html/data/portfolio_history_enriched.json
    """
    logger.info("Step 8: Generating portfolio_history_enriched.json")

    out_path = DST / "portfolio_history_enriched.json"

    # --- Load A3 results (CSV) ---
    csv_path = PORTFOLIO_RESULTS_CSV
    if not csv_path.exists():
        logger.warning("  portfolio_results_db.csv not found — skipping")
        return

    try:
        a3_df = pd.read_csv(csv_path)
    except Exception as e:
        logger.error(f"  Failed to read portfolio_results_db.csv: {e}")
        return

    # --- Load C results (JSONL) ---
    c_entries = []
    if OPTIMIZED_HISTORY_JSONL.exists():
        try:
            with open(OPTIMIZED_HISTORY_JSONL, "r", encoding="utf-8") as f:
                for line in f:
                    line = line.strip()
                    if line:
                        c_entries.append(json.loads(line))
        except Exception as e:
            logger.warning(f"  Failed to read JSONL: {e}")

    # Parse C timestamps for proximity matching
    for entry in c_entries:
        try:
            entry["_ts"] = datetime.strptime(entry["timestamp"], "%Y-%m-%d %H:%M:%S")
        except Exception:
            entry["_ts"] = None

    # --- Load momentum from latest_run_summary.json ---
    latest_summary = read_json_safe(LATEST_RUN_SUMMARY_JSON)
    latest_run_id = latest_summary.get("last_updated_run_id", "")
    latest_momentum = None
    bp = latest_summary.get("best_portfolio_details", {})
    mv = bp.get("momentum_valuation", {})
    if mv:
        latest_momentum = mv.get("portfolio_momentum")

    # --- Build enriched entries ---
    enriched = []

    for _, row in a3_df.iterrows():
        run_id = str(row.get("run_id", ""))
        timestamp_str = str(row.get("timestamp", ""))

        # Parse stocks
        stocks_raw = str(row.get("stocks", ""))
        stocks = [
            s.strip()
            for s in stocks_raw.replace("'", "").replace("[", "").replace("]", "").split(",")
            if s.strip()
        ]

        # Parse weights
        weights_raw = str(row.get("weights", ""))
        weights = []
        for w in weights_raw.replace("[", "").replace("]", "").split(","):
            try:
                weights.append(float(w.strip()))
            except (ValueError, TypeError):
                pass

        entry: Dict[str, Any] = {
            "run_id": run_id,
            "timestamp": timestamp_str,
            "n_assets": len(stocks),
            "stocks": stocks,
            "weights": weights,
            # A3 backward-looking
            "sharpe_forward": safe_float(row.get("sharpe_forward"), None),
            "ret_hist_12m": safe_float(row.get("expected_return_annual_pct"), None),
            "vol_12m": safe_float(row.get("expected_volatility_annual_pct"), None),
            # C forward-looking (filled below via timestamp match)
            "ret_proj_12m": None,
            "score": None,
            "momentum": None,
            # Detail for expandable panel
            "decision": None,
            "transition_cost_pct": None,
            "holdings_return_pct": None,
            "excess_return_pct": None,
        }

        # --- Match with C engine entry by timestamp proximity (±30 min) ---
        if timestamp_str and c_entries:
            try:
                a3_ts = datetime.strptime(timestamp_str, "%Y-%m-%d %H:%M:%S")
                best_match = None
                best_delta = timedelta(minutes=30)

                for c in c_entries:
                    if c.get("_ts") is None:
                        continue
                    delta = abs(c["_ts"] - a3_ts)
                    if delta < best_delta:
                        best_delta = delta
                        best_match = c

                if best_match:
                    entry["ret_proj_12m"] = best_match.get("ideal_return_pct")
                    entry["score"] = best_match.get("ideal_score")
                    entry["decision"] = best_match.get("decision")
                    entry["transition_cost_pct"] = best_match.get("transition_cost_pct")
                    entry["holdings_return_pct"] = best_match.get("holdings_return_pct")
                    entry["excess_return_pct"] = best_match.get("excess_return_pct")
            except Exception:
                pass

        # Momentum: only available for latest run (not persisted per-run)
        if run_id == latest_run_id and latest_momentum is not None:
            entry["momentum"] = latest_momentum

        enriched.append(entry)

    # Sort by run_id descending (newest first)
    enriched.sort(key=lambda x: x.get("run_id", ""), reverse=True)

    write_json(out_path, enriched)
    logger.info(f"  Saved portfolio_history_enriched.json ({len(enriched)} runs)")


def main() -> int:
    start_time = datetime.now()
    logger.info(f"D_Publish.py v{D_PUBLISH_VERSION} — Starting frontend asset publication")
    logger.info(f"Source: {SRC}")
    logger.info(f"Target: {DST}")

    DST.mkdir(parents=True, exist_ok=True)

    try:
        # Step 0: Generate derived snapshot JSONs from history CSVs
        publish_derived_snapshots()

        # Step 1: Symlink canonical files
        publish_symlink_files()

        # Step 2: Generate scored_targets.json
        target_map = publish_scored_targets()

        # Step 3: Generate pipeline_latest.json
        publish_pipeline_latest(target_map)

        # Step 4a: Generate portfolio_real_daily.csv (Mudança 1)
        daily_df = _build_real_daily_series()

        # Step 4b: Compute real TWR metrics (Mudança 2)
        real_metrics = _compute_real_metrics(daily_df)

        # Step 4b2: Compute risk windows (TE, IR, HHI per time window)
        hist_df = pd.read_csv(PORTFOLIO_HISTORY_CSV) if PORTFOLIO_HISTORY_CSV.exists() else pd.DataFrame()
        risk_windows = _compute_risk_windows(daily_df, hist_df)


        # Step 4b4: Compute TWR monthly returns (método da cota)
        logger.info("Step 4b4: Computing TWR monthly returns (quota method)")
        monthly_returns = compute_monthly_twr_from_daily(daily_df)
        if monthly_returns:
            logger.info(
                f"  TWR monthly: {len(monthly_returns.get('meses', []))} months, "
                f"total={monthly_returns.get('twr_total', 0):+.4f}"
            )

        # Step 4c: Generate consolidated dashboard JSON (Mudança 3)
        publish_dashboard_latest(real_metrics, risk_windows, monthly_returns)

        # Step 5: Processed notes
        publish_processed_notes()

        # Step 6: Progress file symlinks
        publish_progress_symlinks()

        # Step 7: Correlation heatmap JSON (portfolio holdings vs model tickers)
        publish_correlation_heatmap()

        # Step 8: Enriched history JSON (A3 + C merged for frontend)
        publish_history_enriched()

        elapsed = (datetime.now() - start_time).total_seconds()
        logger.info(f"D_Publish.py completed in {elapsed:.1f}s")
        return 0

    except Exception as e:
        logger.critical(f"D_Publish.py failed: {e}", exc_info=True)
        return 1


