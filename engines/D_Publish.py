# STEP 4b: COMPUTE REAL TWR METRICS (Mudança 2)
# ═══════════════════════════════════════════════════════════════════════════════


def _compute_real_metrics(daily_df: pd.DataFrame) -> dict:
    """Compute TWR-based metrics from the real daily series.

    Returns a dict with twr, benchmark, alpha, and relative sub-dicts.
    """
    if daily_df.empty:
        return {}

    # Extract return series (skip first row which has no return)
    port_rets = pd.to_numeric(daily_df["portfolio_return"], errors="coerce").dropna()
    bench_rets = pd.to_numeric(daily_df["benchmark_return"], errors="coerce").dropna()

    if port_rets.empty:
        return {}

    n = len(port_rets)

    # --- CDI metrics (real data from LFTS11 or SELIC fallback) ---
    # Computed first so we can use actual CDI annual rate as risk-free
    cdi_rets = pd.to_numeric(daily_df["cdi_return"], errors="coerce").dropna()
    if not cdi_rets.empty:
        cdi_total = float(np.prod(1 + cdi_rets.values) - 1)
        cdi_annual = float((1 + cdi_total) ** (252 / len(cdi_rets)) - 1) if len(cdi_rets) > 0 else SELIC_ANNUAL_FALLBACK
        cdi_source = CDI_PROXY_TICKER
    else:
        cdi_daily_fb = (1 + SELIC_ANNUAL_FALLBACK) ** (1 / 252) - 1
        cdi_total = (1 + cdi_daily_fb) ** n - 1
        cdi_annual = SELIC_ANNUAL_FALLBACK
        cdi_source = "SELIC flat rate (fallback)"

    risk_free_annual = cdi_annual  # Use real CDI rate as risk-free

    # --- Portfolio TWR metrics ---
    total_return = float(np.prod(1 + port_rets.values) - 1)
    annual_return = float((1 + total_return) ** (252 / n) - 1) if n > 0 else 0.0
    volatility = float(port_rets.std(ddof=1) * np.sqrt(252)) if n > 1 else 0.0
    sharpe = float((annual_return - risk_free_annual) / volatility) if volatility > 0 else None
    
    # Sortino
    downside = port_rets[port_rets < 0]
    downside_dev = float(downside.std(ddof=1) * np.sqrt(252)) if len(downside) > 1 else 0.0
    sortino = float((annual_return - risk_free_annual) / downside_dev) if downside_dev > 0 else None

    # Max drawdown (compute from TWR index)
    idx = np.cumprod(1 + port_rets.values)
    peak = np.maximum.accumulate(idx)
    dd = (idx - peak) / peak
    max_drawdown = float(abs(dd.min())) if len(dd) > 0 else 0.0

    # Calmar
    calmar = float(annual_return / max_drawdown) if max_drawdown > 0 else None

    # --- Benchmark metrics (same dates) ---
    # Align benchmark to same index as portfolio returns
    common_idx = port_rets.index.intersection(bench_rets.index)
    port_common = port_rets.loc[common_idx]
    bench_common = bench_rets.loc[common_idx]

    bench_total = float(np.prod(1 + bench_common.values) - 1) if len(bench_common) > 0 else 0.0
    n_bench = len(bench_common)
    bench_annual = float((1 + bench_total) ** (252 / n_bench) - 1) if n_bench > 0 else 0.0

    # --- Alpha ---
    alpha_total = total_return - bench_total
    alpha_annual = annual_return - bench_annual

    # --- Relative metrics ---
    beta = None
    tracking_error = None
    information_ratio = None
    correlation = None

    if len(port_common) > 1 and bench_common.std() > 0:
        cov_matrix = np.cov(port_common.values, bench_common.values)
        beta = float(cov_matrix[0, 1] / cov_matrix[1, 1])

        diff = port_common.values - bench_common.values
        tracking_error = float(np.std(diff, ddof=1) * np.sqrt(252))
        if tracking_error > 0:
            information_ratio = float(alpha_annual / tracking_error)

        corr_val = np.corrcoef(port_common.values, bench_common.values)[0, 1]
        correlation = float(corr_val) if np.isfinite(corr_val) else None

    # % do CDI: standard Brazilian fund metric
    pct_cdi = round((total_return / cdi_total) * 100, 1) if cdi_total > 0 else None

    # Annualization safety flag: annualized numbers are unreliable under 1 year
    annualize_safe = n >= 252

    # --- Performance windows ---
    performance = _compute_performance_windows(daily_df)

    return {
        "twr": {
            "total_return": round(total_return, 4),
            "annual_return": round(annual_return, 4),
            "volatility": round(volatility, 4),
            "sharpe": round(sharpe, 2) if sharpe is not None else None,
            "sortino": round(sortino, 2) if sortino is not None else None,
            "max_drawdown": round(max_drawdown, 4),
            "calmar": round(calmar, 2) if calmar is not None else None,
            "pct_cdi": pct_cdi,
            "risk_free_annual": round(risk_free_annual, 4),
        },
        "benchmark": {
            "ticker": "^BVSP",
            "total_return": round(bench_total, 4),
            "annual_return": round(bench_annual, 4),
        },
        "cdi": {
            "total_return": round(cdi_total, 4),
            "annual_rate": round(cdi_annual, 4),
            "source": cdi_source,
        },
        "alpha": {
            "total": round(alpha_total, 4),
            "annual": round(alpha_annual, 4),
        },
        "relative": {
            "beta": round(beta, 2) if beta is not None else None,
            "tracking_error": round(tracking_error, 4) if tracking_error is not None else None,
            "information_ratio": round(information_ratio, 2) if information_ratio is not None else None,
            "correlation": round(correlation, 2) if correlation is not None else None,
        },
        "annualize_safe": annualize_safe,
        "performance": performance,
    }


def _compute_performance_windows(daily_df: pd.DataFrame) -> dict:
    """Compute portfolio, benchmark & CDI returns for standard time windows.

    Windows: YTD, 3M, 6M, 12M, 24M — computed from the real daily series.
    Each window includes portfolio, benchmark, cdi, and pct_cdi.
    """
    if daily_df.empty:
        return {}

    df = daily_df.copy()
    df["date_dt"] = pd.to_datetime(df["date"])
    df["port_r"] = pd.to_numeric(df["portfolio_return"], errors="coerce")
    df["bench_r"] = pd.to_numeric(df["benchmark_return"], errors="coerce")
    df["cdi_r"] = pd.to_numeric(df["cdi_return"], errors="coerce")

    last_date = df["date_dt"].max()
    result = {}


    windows = {
        "YTD": pd.Timestamp(last_date.year, 1, 1),
        "3M": last_date - pd.DateOffset(months=3),
        "6M": last_date - pd.DateOffset(months=6),
        "12M": last_date - pd.DateOffset(months=12),
        "24M": last_date - pd.DateOffset(months=24),
    }

    for label, start in windows.items():
        subset = df[df["date_dt"] >= start]
        port_r = subset["port_r"].dropna()
        bench_r = subset["bench_r"].dropna()
        cdi_r = subset["cdi_r"].dropna()

        if port_r.empty:
            continue

        p_total = float(np.prod(1 + port_r.values) - 1)
        b_total = float(np.prod(1 + bench_r.values) - 1) if not bench_r.empty else None
        c_total = float(np.prod(1 + cdi_r.values) - 1) if not cdi_r.empty else None
        pct_cdi = round((p_total / c_total) * 100, 1) if c_total and c_total > 0 else None

        result[label] = {
            "portfolio": round(p_total, 4),
            "benchmark": round(b_total, 4) if b_total is not None else None,
            "cdi": round(c_total, 4),
            "pct_cdi": pct_cdi,
        }

    return result



def _compute_risk_windows(daily_df: pd.DataFrame, hist_df: pd.DataFrame) -> dict:
    """Compute Tracking Error, Information Ratio, and HHI per time window.

    Windows: All, YTD, 3M, 6M, 12M, 24M.
    TE and IR are derived from daily returns in daily_df.
    HHI is a point-in-time snapshot from the last date in hist_df within each window.
    """
    if daily_df.empty:
        return {}

    df = daily_df.copy()
    df["date_dt"] = pd.to_datetime(df["date"])
    df["port_r"] = pd.to_numeric(df["portfolio_return"], errors="coerce")
    df["bench_r"] = pd.to_numeric(df["benchmark_return"], errors="coerce")

    last_date = df["date_dt"].max()

    # Prepare hist_df for HHI lookups
    hist_has_data = hist_df is not None and not hist_df.empty
    if hist_has_data:
        hist_df = hist_df.copy()
        hist_df["date_dt"] = pd.to_datetime(hist_df["date"])
        hist_df["value_f"] = pd.to_numeric(hist_df["value"], errors="coerce")

    windows = {
        "All": df["date_dt"].min(),
        "YTD": pd.Timestamp(last_date.year, 1, 1),
        "3M": last_date - pd.DateOffset(months=3),
        "6M": last_date - pd.DateOffset(months=6),
        "12M": last_date - pd.DateOffset(months=12),
        "24M": last_date - pd.DateOffset(months=24),
    }

    result = {}

    for label, start in windows.items():
        subset = df[df["date_dt"] >= start]
        port_r = subset["port_r"].dropna()
        bench_r = subset["bench_r"].dropna()

        if len(port_r) < 2 or len(bench_r) < 2:
            continue

        # Align to common dates
        common_idx = port_r.index.intersection(bench_r.index)
        port_common = port_r.loc[common_idx]
        bench_common = bench_r.loc[common_idx]
        n = len(port_common)

        if n < 2:
            continue

        # Tracking Error
        diff = port_common.values - bench_common.values
        te = float(np.std(diff, ddof=1) * np.sqrt(252))

        # Alpha annual
        port_total = float(np.prod(1 + port_common.values) - 1)
        bench_total = float(np.prod(1 + bench_common.values) - 1)
        port_annual = float((1 + port_total) ** (252 / n) - 1) if n > 0 else 0.0
        bench_annual = float((1 + bench_total) ** (252 / n) - 1) if n > 0 else 0.0
        alpha_annual = port_annual - bench_annual

        # Information Ratio
        ir = float(alpha_annual / te) if te > 0 else None

        # HHI: point-in-time from last date in hist_df within this window
        hhi = None
        n_assets = None
        if hist_has_data:
            hist_window = hist_df[hist_df["date_dt"] >= start]
            if not hist_window.empty:
                last_hist_date = hist_window["date_dt"].max()
                last_day = hist_window[hist_window["date_dt"] == last_hist_date]
                values = last_day["value_f"].dropna()
                total_val = values.sum()
                if total_val > 0:
                    w = values / total_val
                    hhi = round(float((w ** 2).sum()), 4)
                    n_assets = int((values > 0).sum())

        entry: Dict[str, Any] = {
            "tracking_error": round(te, 4),
            "information_ratio": round(ir, 2) if ir is not None else None,
            "hhi": hhi,
            "n_assets": n_assets,
            "trading_days": n,
        }
        result[label] = entry

    return result


