def _load_latest_prices() -> Dict[str, float]:
    """Stream StockDataDB.csv and keep last close price per symbol."""
    result: Dict[str, float] = {}
    if not STOCK_DATA_DB.exists():
        return result
    try:
        with open(STOCK_DATA_DB, encoding="utf-8", errors="replace") as fh:
            reader = csv.DictReader(fh)
            for row in reader:
                stock = (row.get("Stock") or "").strip()
                close_str = (row.get("Close") or "").strip()
                if stock and close_str:
                    try:
                        result[stock] = float(close_str)
                    except ValueError:
                        pass
    except Exception as e:
        logger.warning(f"  Could not load prices: {e}")
    return result


def _load_findb_target_prices() -> Dict[str, float]:
    """Read the latest targetMeanPrice per stock from FinancialsDB.csv.

    Used as a fallback for stocks that were filtered out of scored_stocks.csv
    (e.g. PETR3 when its target is below current price → negative upside → excluded
    from scoring but still present in the model portfolio).
    Returns a dict keyed by normalize_ticker(stock).
    """
    result: Dict[str, float] = {}
    findb = SRC / "findb" / "FinancialsDB.csv"
    if not findb.exists():
        return result
    try:
        df = pd.read_csv(findb)
        df["LastUpdated"] = pd.to_datetime(df["LastUpdated"], errors="coerce")
        latest = df.sort_values("LastUpdated").drop_duplicates(subset="Stock", keep="last")
        for _, row in latest.iterrows():
            tp = safe_float(row.get("targetMeanPrice"), 0.0)
            if tp > 0:
                result[normalize_ticker(str(row.get("Stock", "")))] = tp
    except Exception as e:
        logger.warning(f"  Could not load targetMeanPrice from FinancialsDB: {e}")
    return result


def _load_broker_name_mapping() -> Dict[str, str]:
    """Load BrokerName → Ticker mapping from tickers.txt."""
    mapping: Dict[str, str] = {}
    if not TICKERS_FILE.exists():
        return mapping
    try:
        with open(TICKERS_FILE, encoding="utf-8") as fh:
            reader = csv.DictReader(fh)
            for row in reader:
                ticker = (row.get("Ticker") or "").strip()
                broker_name = (row.get("BrokerName") or "").strip()
                name = (row.get("Name") or "").strip()
                if ticker:
                    base = ticker.replace(".SA", "").strip()
                    mapping[normalize_ticker(base)] = ticker
                    if broker_name:
                        mapping[normalize_ticker(broker_name)] = ticker
                    if name:
                        mapping[normalize_ticker(name)] = ticker
    except Exception as e:
        logger.warning(f"  Could not load tickers mapping: {e}")
    return mapping


def _load_scored_stocks_meta() -> Dict[str, Dict[str, Any]]:
    """Load metadata from scored_stocks.csv for enrichment."""
    meta = {}
    if SCORED_STOCKS_CSV.exists():
        try:
            df = pd.read_csv(SCORED_STOCKS_CSV)
            for _, row in df.iterrows():
                ticker = normalize_ticker(str(row.get("Stock", "")))
                meta[ticker] = {
                    "composite_score": safe_float(row.get("CompositeScore")),
                    "momentum_score": safe_float(row.get("Momentum")),
                    "upside_pct": safe_float(row.get("PotentialUpside_pct")),
                    "annual_ret": safe_float(row.get("AnnualizedMeanReturn")),
                    "annual_vol": safe_float(row.get("AnnualizedStdDev")),
                }
        except Exception as e:
            logger.warning(f"  Could not load scored_stocks meta: {e}")
    return meta


def publish_pipeline_latest(target_map: Dict[str, float]) -> None:
    """Generate pipeline_latest.json: model portfolio projected onto real values."""
    logger.info("Step 3: Generating pipeline_latest.json")

    # Load latest_run_summary.json for model stocks/weights
    summary = read_json_safe(resolve_source(LATEST_RUN_SUMMARY_JSON))
    bp = summary.get("best_portfolio_details", {})
    stocks = bp.get("stocks", [])
    weights = bp.get("weights", [])

    if not stocks:
        # Fallback: try portfolio_results_db.csv
        if PORTFOLIO_RESULTS_CSV.exists():
            try:
                df = pd.read_csv(PORTFOLIO_RESULTS_CSV)
                if not df.empty:
                    last_row = df.iloc[-1]
                    stocks_str = str(last_row.get("stocks", ""))
                    weights_str = str(last_row.get("weights", ""))
                    stocks = [s.strip() for s in stocks_str.split(",") if s.strip()]
                    weights = [safe_float(w) for w in weights_str.split(",") if w.strip()]
            except Exception:
                pass

    if not stocks:
        write_json(PIPELINE_LATEST_JSON, {
            "stocks": [], "weights": [], "rows": [], "totals": {},
            "generated_at": datetime.now(timezone.utc).isoformat(),
        })
        ensure_symlink(PIPELINE_LATEST_JSON, DST)
        return

    # Normalize weights to percentages if they look like fractions
    if weights:
        s = sum(weights)
        if 0 < s <= 1.1:
            weights = [w * 100.0 for w in weights]

    # Get available capital from ledger_positions.json
    ledger_data = read_json_safe(resolve_source(LEDGER_POSITIONS_JSON))
    total_market = safe_float(ledger_data.get("total_current_market"), 0.0)
    total_invested = safe_float(ledger_data.get("total_invested_cash"), 0.0)
    available_capital = total_market if total_market > 0 else total_invested

    # Load prices
    price_map = _load_latest_prices()
    # Fallback target prices from FinancialsDB (covers stocks filtered out of scoring,
    # e.g. those with negative upside like PETR3 when target < current price)
    findb_targets = _load_findb_target_prices()
    # Enrichment: Scored stocks meta (scores, momentum, performance)
    scored_meta = _load_scored_stocks_meta()

    # Build rows
    rows = []
    totals = {
        "totalPct": 0.0,
        "totalCurrentSum": 0.0,
        "totalProjectedQty": 0,
        "totalProjectedInvested": 0.0,
        "totalProjectedBRL": 0.0,
    }

    for i, stock in enumerate(stocks):
        w = safe_float(weights[i] if i < len(weights) else 0)
        totals["totalPct"] += w

        allocated = (available_capital * (w / 100.0)) if available_capital > 0 else None

        # Find current price
        norm = normalize_ticker(stock)
        current = price_map.get(stock)
        if current is None:
            for k, v in price_map.items():
                if normalize_ticker(k) == norm:
                    current = v
                    break

        # Find target price: prefer scored_targets (upside > 0), fall back to FinancialsDB
        tp = target_map.get(norm) or findb_targets.get(norm)

        # Calculate projections
        projected_qty = None
        projected_invested = None
        projected_brl = None

        if allocated is not None and current and current > 0:
            projected_qty = int(math.floor(allocated / current))
            projected_invested = projected_qty * current
        if projected_qty is not None and tp:
            projected_brl = projected_qty * tp

        if allocated:
            totals["totalCurrentSum"] += allocated
        if projected_qty:
            totals["totalProjectedQty"] += projected_qty
        if projected_invested:
            totals["totalProjectedInvested"] += projected_invested
        if projected_brl:
            totals["totalProjectedBRL"] += projected_brl

        # Find enrichment data
        meta = scored_meta.get(norm, {})

        rows.append({
            "ticker": stock,
            "weight": w,
            "current": current,
            "projectedQty": projected_qty,
            "projectedInvested": projected_invested,
            "target": tp,
            "projectedBRL": projected_brl,
            # Enriched fields
            "composite_score": meta.get("composite_score"),
            "momentum_score": meta.get("momentum_score"),
            "upside_pct": meta.get("upside_pct"),
            "annual_ret": meta.get("annual_ret"),
            "annual_vol": meta.get("annual_vol"),
        })

    rows.sort(key=lambda r: (r.get("projectedInvested") or 0), reverse=True)

    write_json(PIPELINE_LATEST_JSON, {
        "stocks": stocks,
        "weights": weights,
        "rows": rows,
        "totals": totals,
        "used_path": str(LATEST_RUN_SUMMARY_JSON),
        "generated_at": datetime.now(timezone.utc).isoformat(),
    })
    ensure_symlink(PIPELINE_LATEST_JSON, DST)


