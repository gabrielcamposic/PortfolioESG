# STEP 7: SYMLINK PROGRESS FILES
# ═══════════════════════════════════════════════════════════════════════════════


def publish_progress_symlinks() -> None:
    """Ensure progress JSONs in data/ are symlinked into html/data/.

    Progress files are written live by engines during execution.
    If the canonical file in data/ doesn't exist yet, create an empty
    placeholder so the symlink target is valid.
    """
    logger.info("Step 7: Symlinking progress files")
    for name in PROGRESS_FILES:
        canonical = PROGRESS_DIR / name
        # If progress file only exists in html/data/ (transition), move it
        dst = DST / name
        if not canonical.exists() and dst.exists() and not dst.is_symlink():
            dst.rename(canonical)
            logger.info(f"  Migrated {name} → data/")
        # Create empty placeholder if needed
        if not canonical.exists():
            canonical.parent.mkdir(parents=True, exist_ok=True)
            canonical.write_text("{}", encoding="utf-8")
            logger.info(f"  Created empty {name}")
        ensure_symlink(canonical, DST)


