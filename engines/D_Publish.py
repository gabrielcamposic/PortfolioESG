def _build_model_section() -> dict:
    """Build the 'model' section of dashboard_latest.json."""
    latest_summary = read_json_safe(resolve_source(LATEST_RUN_SUMMARY_JSON))
    optimized = read_json_safe(resolve_source(OPTIMIZED_RECOMMENDATION_JSON))

    bp = latest_summary.get("best_portfolio_details", {})
    comparison = optimized.get("comparison", {})
    holdings = comparison.get("holdings", {})
    ideal = comparison.get("ideal", {})
    optimal = comparison.get("optimal", {})
    momentum_val = bp.get("momentum_valuation", {})

    # Returns
    expected_return_hold = safe_float(holdings.get("expected_return_pct"), 0.0)
    expected_return_gross = safe_float(ideal.get("expected_return_pct"), 0.0)
    historical_return = safe_float(
        ideal.get("historical_return_pct"),
        safe_float(bp.get("expected_return_annual_pct"), 0.0),
    )
    expected_volatility = safe_float(bp.get("expected_volatility_annual_pct"), 0.0)

    # Sharpe
    risk_free = 11.75  # SELIC annual %
    sharpe_forward = None
    if expected_volatility > 0:
        sharpe_forward = (expected_return_gross - risk_free) / expected_volatility
    sharpe_backtest = bp.get("sharpe_forward", bp.get("sharpe_ratio"))
    sharpe = sharpe_forward if sharpe_forward is not None else sharpe_backtest

    # Decision
    cost_pct = safe_float(optimal.get("transition_cost_pct"), 0.0)
    net_return = expected_return_gross - cost_pct
    excess_return = net_return - expected_return_hold
    threshold = safe_float(optimized.get("min_threshold_pct"), 0.5)
    decision = "REBALANCE" if excess_return > threshold else "HOLD"

    return {
        "meta": {
            "run_id": latest_summary.get("last_updated_run_id", ""),
            "timestamp": latest_summary.get("last_updated_timestamp", ""),
        },
        "returns": {
            "hold_12m": expected_return_hold,
            "gross_12m": expected_return_gross,
            "net_12m": net_return,
            "historical_annual": historical_return,
            "excess_net_12m": excess_return,
        },
        "risk": {
            "volatility_annual": expected_volatility,
            "risk_free_annual": risk_free,
            "sharpe": sharpe,
            "sharpe_source": "forward" if sharpe_forward is not None else "backtest",
            "hhi": bp.get("concentration_risk", {}).get("hhi"),
            "top5": bp.get("concentration_risk", {}).get("top_5_holdings_pct"),
        },
        "decision": {
            "verdict": decision,
            "threshold_pct": threshold,
            "intensity": safe_float(optimal.get("blend_ratio"), 0.0) * 100,
        },
        "valuation": {
            "portfolio_pe": momentum_val.get("portfolio_forward_pe"),
            "benchmark_pe": momentum_val.get("benchmark_forward_pe"),
            "dividend_yield": momentum_val.get("portfolio_dividend_yield"),
            "momentum_signal": momentum_val.get("portfolio_momentum"),
        },
        "composition": {
            "sector_exposure": bp.get("sector_exposure_list", []),
        },
    }


def _build_real_section(real_metrics: dict, risk_windows: Optional[dict] = None,
                        broker_return: Optional[dict] = None) -> dict:
    """Build the 'real' section of dashboard_latest.json (Mudança 3).

    Uses TWR metrics computed from the real daily series instead of
    model diagnostics from A4.
    """
    # Read portfolio history CSV for date range
    history_start = None
    history_end = None
    trading_days = 0
    if PORTFOLIO_HISTORY_CSV.exists():
        try:
            hist_df = pd.read_csv(PORTFOLIO_HISTORY_CSV, usecols=["date"])
            if not hist_df.empty:
                unique_dates = hist_df["date"].dropna().unique()
                unique_dates.sort()
                history_start = str(unique_dates[0]) if len(unique_dates) > 0 else None
                history_end = str(unique_dates[-1]) if len(unique_dates) > 0 else None
                trading_days = len(unique_dates)
        except Exception as e:
            logger.warning(f"  Could not read portfolio_history.csv: {e}")

    # Compute coverage: tracked portfolio value vs ledger total
    coverage_pct = None
    ledger_data = read_json_safe(resolve_source(LEDGER_POSITIONS_JSON))
    total_ledger_market = safe_float(ledger_data.get("total_current_market"), 0.0)
    total_ledger_invested = safe_float(ledger_data.get("total_invested_cash"), 0.0)

    if PORTFOLIO_HISTORY_CSV.exists() and total_ledger_market > 0:
        try:
            hist_df = pd.read_csv(PORTFOLIO_HISTORY_CSV)
            if not hist_df.empty:
                last_date = hist_df["date"].max()
                last_day = hist_df[hist_df["date"] == last_date]
                tracked_value = last_day["market_value"].iloc[0] if not last_day.empty else 0
                coverage_pct = round((tracked_value / total_ledger_market) * 100, 1)
        except Exception:
            pass

    # Snapshot from ledger_positions.json
    n_positions = len(ledger_data.get("positions", []))
    total_pnl = safe_float(ledger_data.get("total_unrealized_pnl"), 0.0)
    simple_roi = (total_pnl / total_ledger_invested * 100) if total_ledger_invested > 0 else 0.0

    # Structure from ledger_positions.json — REAL portfolio concentration
    structure = {}
    positions = ledger_data.get("positions", [])
    if positions and total_ledger_market > 0:
        weights = []
        for pos in positions:
            qty = safe_float(pos.get("net_qty"), 0.0)
            price = safe_float(pos.get("current_price"), 0.0)
            mv = qty * price
            if mv > 0:
                weights.append(mv / total_ledger_market)
        weights_sorted = sorted(weights, reverse=True)
        structure = {
            "hhi": round(sum(w ** 2 for w in weights), 4),
            "top3": round(sum(weights_sorted[:3]), 4) if len(weights_sorted) >= 3 else round(sum(weights_sorted), 4),
            "top5": round(sum(weights_sorted[:5]), 4) if len(weights_sorted) >= 5 else round(sum(weights_sorted), 4),
            "n_assets": len(weights),
        }

    real: Dict[str, Any] = {
        "meta": {
            "history_start": history_start,
            "history_end": history_end,
            "trading_days": trading_days,
            "coverage_pct": coverage_pct,
            "annualize_safe": real_metrics.get("annualize_safe", False),
            "source": "portfolio_history.csv + ledger.csv",
        },
        "snapshot": {
            "total_market": round(total_ledger_market, 2),
            "total_invested": round(total_ledger_invested, 2),
            "unrealized_pnl": round(total_pnl, 2),
            "simple_roi_pct": round(simple_roi, 2),
            "n_positions": n_positions,
        },
        "twr": real_metrics.get("twr", {}),
        "benchmark": real_metrics.get("benchmark", {}),
        "cdi": real_metrics.get("cdi", {}),
        "alpha": real_metrics.get("alpha", {}),
        "relative": real_metrics.get("relative", {}),
        "structure": structure,
        "risk_windows": risk_windows or {},
        "performance": real_metrics.get("performance", {}),
        "broker_return": broker_return or {},
    }

    return real


def publish_dashboard_latest(real_metrics: dict, risk_windows: Optional[dict] = None,
                             broker_return: Optional[dict] = None) -> None:
    """Generate dashboard_latest.json consolidating model and real portfolio views.

    Replaces the former separate mis_model_latest.json and mis_real_latest.json.
    The 'real' section now uses TWR metrics computed from portfolio_real_daily.csv.
    """
    logger.info("Step 4c: Generating dashboard_latest.json")

    dashboard = {
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "model": _build_model_section(),
        "real": _build_real_section(real_metrics, risk_windows, broker_return),
    }

    write_json(DASHBOARD_LATEST_JSON, dashboard)
    ensure_symlink(DASHBOARD_LATEST_JSON, DST)

    # Symlink history CSVs for frontend access
    ensure_symlink(PORTFOLIO_RESULTS_CSV, DST, "mis_model_history.csv")
    ensure_symlink(DIAGNOSTICS_HISTORY_CSV, DST, "mis_real_history.csv")
    ensure_symlink(ASSET_ATTRIBUTION_CSV, DST, "mis_real_attribution.csv")


