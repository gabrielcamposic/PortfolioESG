# CDI DAILY RETURNS LOADER
# ═══════════════════════════════════════════════════════════════════════════════


def _load_cdi_daily_returns() -> Tuple[Dict[str, float], str]:
    """Load real CDI daily returns from LFTS11.SA in StockDataDB.csv.

    Returns:
        (dict mapping date_str → daily_return, source_label)
    Falls back to SELIC synthetic flat rate if LFTS11 data is unavailable.
    """
    cdi_by_date: Dict[str, float] = {}
    source = "SELIC flat rate (fallback)"

    if STOCK_DATA_DB.exists():
        try:
            df = pd.read_csv(STOCK_DATA_DB)
            cdi_df = df[df["Stock"] == CDI_PROXY_TICKER].copy()
            if not cdi_df.empty:
                cdi_df["Date"] = pd.to_datetime(cdi_df["Date"], errors="coerce")
                cdi_df = cdi_df.dropna(subset=["Date"]).sort_values("Date")
                cdi_df["daily_return"] = cdi_df["Close"].pct_change()
                for _, row in cdi_df.iterrows():
                    dt = row["Date"].strftime("%Y-%m-%d")
                    dr = row["daily_return"]
                    if pd.notna(dr):
                        cdi_by_date[dt] = float(dr)
                source = f"{CDI_PROXY_TICKER} (real)"
                logger.info(f"  Loaded {len(cdi_by_date)} CDI daily returns from {CDI_PROXY_TICKER}")
        except Exception as e:
            logger.warning(f"  Could not load CDI data from StockDataDB: {e}")

    if not cdi_by_date:
        logger.warning(f"  {CDI_PROXY_TICKER} not found in StockDataDB — using SELIC flat rate fallback")

    return cdi_by_date, source


