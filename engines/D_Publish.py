def publish_mis_real() -> None:
    """Generate mis_real_latest.json from diagnostics history and portfolio history."""
    logger.info("Step 5: Generating mis_real_latest.json")

    # Read latest diagnostics row
    diag = latest_csv_rows(DIAGNOSTICS_HISTORY_CSV)
    # Read latest performance windows row
    perf_win = latest_csv_rows(PERF_WINDOWS_CSV)
    # Read portfolio history for basic stats
    history_data = read_json_safe(resolve_source(PORTFOLIO_HISTORY_JSON))
    history = history_data.get("history", []) if isinstance(history_data, dict) else []

    # Build real MIS from diagnostics CSV (populated by A4_Analysis)
    real_latest: Dict[str, Any] = {
        "meta": {
            "generated_at": datetime.now(timezone.utc).isoformat(),
            "history_start": history[0].get("date") if history else None,
            "history_end": history[-1].get("date") if history else None,
            "observations": len(history),
        },
    }

    if not diag.empty:
        row = diag.iloc[0]
        real_latest["portfolio"] = {
            "total_return_twr": safe_float(row.get("twr_total_return")),
            "annual_return": safe_float(row.get("annual_return")),
            "volatility": safe_float(row.get("annual_volatility")),
            "sharpe": safe_float(row.get("sharpe")),
            "sortino": safe_float(row.get("sortino")),
            "max_drawdown": safe_float(row.get("max_drawdown")),
            "calmar": safe_float(row.get("calmar")),
        }
        real_latest["relative"] = {
            "beta": safe_float(row.get("beta")),
            "tracking_error": safe_float(row.get("tracking_error")),
            "information_ratio": safe_float(row.get("information_ratio")),
            "correlation": safe_float(row.get("correlation_vs_benchmark")),
        }
        real_latest["structure"] = {
            "hhi": safe_float(row.get("hhi")),
            "top3": safe_float(row.get("top3_concentration")),
            "top5": safe_float(row.get("top5_concentration")),
            "n_assets": int(safe_float(row.get("n_assets"), 0)),
        }
    else:
        real_latest["portfolio"] = {}
        real_latest["relative"] = {}
        real_latest["structure"] = {}

    # Performance windows
    if not perf_win.empty:
        row = perf_win.iloc[0]
        real_latest["performance"] = {}
        for window in ["ytd", "3m", "6m", "12m", "24m"]:
            real_latest["performance"][window.upper()] = {
                "portfolio": safe_float(row.get(f"perf_{window}_portfolio")),
                "benchmark": safe_float(row.get(f"perf_{window}_benchmark")),
            }
    else:
        real_latest["performance"] = {}

    write_json(MIS_REAL_LATEST_JSON, real_latest)
    ensure_symlink(MIS_REAL_LATEST_JSON, DST)

    # Symlink history CSVs
    ensure_symlink(DIAGNOSTICS_HISTORY_CSV, DST, "mis_real_history.csv")
    ensure_symlink(ASSET_ATTRIBUTION_CSV, DST, "mis_real_attribution.csv")


