def publish_scored_targets() -> Dict[str, float]:
    """Generate scored_targets.json from latest scored_stocks.csv run."""
    logger.info("Step 2: Generating scored_targets.json")

    targets: Dict[str, float] = {}
    symbols: Dict[str, str] = {}

    latest = latest_csv_rows(SCORED_STOCKS_CSV)
    if latest.empty:
        logger.warning("  No scored stocks data available")
        write_json(SCORED_TARGETS_JSON, {
            "targets": {},
            "symbols": {},
            "generated_at": datetime.now(timezone.utc).isoformat(),
        })
        ensure_symlink(SCORED_TARGETS_JSON, DST)
        return targets

    for _, row in latest.iterrows():
        stock = str(row.get("Stock", "")).strip()
        if not stock:
            continue
        tp = safe_float(row.get("TargetPrice"), 0.0)
        if tp > 0:
            key = normalize_ticker(stock)
            targets[key] = tp
            symbols[key] = stock

    write_json(SCORED_TARGETS_JSON, {
        "targets": targets,
        "symbols": symbols,
        "generated_at": datetime.now(timezone.utc).isoformat(),
    })
    ensure_symlink(SCORED_TARGETS_JSON, DST)
    return targets


